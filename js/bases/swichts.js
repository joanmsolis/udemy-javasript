const dia = 5;
switch(dia){
    case 9:
        console.log('domingo');
        break;
    case 1:
        console.log('lunes');
        break;
    case 3:
        console.log('miercoles');
        break;
    case 4:
        console.log('jueves');
        break;
    default:
        console.log('no es ni lunes, ni marte y mucho menos miercoles');    

}